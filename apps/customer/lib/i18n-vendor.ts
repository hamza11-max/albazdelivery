/**
 * Get vendor translation
 */
export function vt(key: string, language: 'fr' | 'ar' = 'fr'): string {
  const translation = vendorTranslations[key as keyof typeof vendorTranslations]
  if (translation && typeof translation === 'object' && language in translation) {
    return translation[language as keyof typeof translation]
  }
  return key
}

/**
 * Create vendor translator function
 */
export function createVendorTranslator(language: 'fr' | 'ar' = 'fr') {
  return (key: string) => vt(key, language)
}

